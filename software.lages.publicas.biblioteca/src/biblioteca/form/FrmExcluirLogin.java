    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int rowIndex = tbLogin.getSelectedRow();
        if (rowIndex == -1) {
            JOptionPane.showMessageDialog(this, "Selecione o usuario que deseja remover!!!");
            return;
        }
        Login login = new LoginTableModel(loginList).get(rowIndex);
        int confirm = JOptionPane.showConfirmDialog(this, "Confirmar exclus√£o ?", "Excluir Login", JOptionPane.YES_NO_OPTION);
        if (confirm != 0) {
            return;
        }
        int result = new LoginController().excluirLogin(login.getId());
        if (result == 1) {
            JOptionPane.showMessageDialog(this, "Login removida com Sucesso!");
            this.refreshTable();
        } else {
            JOptionPane.showMessageDialog(this, "Tente novamente!");
        }
    }//GEN-LAST:event_btExcluirActionPerformed

