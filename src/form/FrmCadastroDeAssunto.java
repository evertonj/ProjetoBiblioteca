    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed

        int result;

        {
            if (new AssuntoController().buscar(tfNome.getText()) != null) {
                JOptionPane.showMessageDialog(this, "Assunto j√° cadastrado");
            } else {

                Assunto assunto = new Assunto();
                assunto.setNome(tfNome.getText());

                if (idAssunto == null) {
                    result = new AssuntoController().addAssunto(assunto);
                } else {
                    assunto.setId(idAssunto);
                    result = new AssuntoController().alterarAssunto(assunto);
                    idAssunto = null;
                }
                if (result == 1) {
                    JOptionPane.showMessageDialog(this, "Assunto inserido com Sucesso!");

                    onCancelar();

                } else {
                    JOptionPane.showMessageDialog(this, "Tente novamente!");
                }
                List<Assunto> listaAssunto = daoAssunto.finAll();
                cbAssunto.setModel(new DefaultComboBoxModel(listaAssunto.toArray()));
                cbAssunto.getModel().setSelectedItem(assunto);
                int indice = listaAssunto.indexOf(assunto);
                cbAssunto.setSelectedIndex(indice);
            }

        }


    }//GEN-LAST:event_btSalvarActionPerformed

